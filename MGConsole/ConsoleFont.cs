        // Draws a glyph for the given ASCII character into the data array
        private void DrawGlyph(Color[] data, int texWidth, int texHeight, int x, int y, int ch)
        {
            int idx = ch - FirstChar;
            if (idx < 0 || idx >= CharCount) return;
            int baseIdx = idx * GlyphHeight;
            for (int row = 0; row < GlyphHeight; row++)
            {
                int fontIdx = baseIdx + row;
                byte bits = fontIdx < font8x16.Length ? font8x16[fontIdx] : (byte)0;
                for (int col = 0; col < GlyphWidth; col++)
                {
                    int px = x + col;
                    int py = y + row;
                    if ((bits & (1 << (7 - col))) != 0)
                    {
                        if (px < texWidth && py < texHeight)
                            data[py * texWidth + px] = Color.White;
                    }
                }
            }
        }

        // Draws a glyph from a raw 16-byte bitmap (one byte per row, MSB = leftmost pixel).
        private static void DrawBits(Color[] data, int texWidth, int texHeight, int x, int y, byte[] bits)
        {
            int rowCount = Math.Min(GlyphHeight, bits.Length);
            for (int row = 0; row < rowCount; row++)
            {
                byte b = bits[row];
                for (int col = 0; col < GlyphWidth; col++)
                {
                    int px = x + col;
                    int py = y + row;
                    if ((b & (1 << (7 - col))) != 0)
                    {
                        if (px < texWidth && py < texHeight)
                            data[py * texWidth + px] = Color.White;
                    }
                }
            }
        }

