        // Builds an 8x16 single-line box-drawing glyph from per-side flags
        // (0 = no line, 1 = single line). Pure double-line and mixed glyphs
        // are hand-coded below for accuracy at this small size.
        // Single line is drawn at center cols 3-4 / rows 7-8.
        private static byte[] BoxGlyph(int top, int bottom, int left, int right)
        {
            byte[] g = new byte[GlyphHeight];
            if (top == 1)    for (int r = 0; r <= 6;  r++) g[r] |= 0x18;
            if (bottom == 1) for (int r = 9; r <= 15; r++) g[r] |= 0x18;
            if (left == 1)   { g[7] |= 0xF8; g[8] |= 0xF8; }
            if (right == 1)  { g[7] |= 0x1F; g[8] |= 0x1F; }
            // Center crossing pixel (always present when any side is set).
            if (top != 0 || bottom != 0 || left != 0 || right != 0)
            {
                g[7] |= 0x18;
                g[8] |= 0x18;
            }
            return g;
        }

        // Filled block covering the given row range [rowStart, rowEnd).
        private static byte[] BlockRows(int rowStart, int rowEnd)
        {
            byte[] g = new byte[GlyphHeight];
            for (int r = rowStart; r < rowEnd && r < GlyphHeight; r++) g[r] = 0xFF;
            return g;
        }

        // Filled block covering the given column range [colStart, colEnd).
        private static byte[] BlockCols(int colStart, int colEnd)
        {
            byte[] g = new byte[GlyphHeight];
            byte mask = 0;
            for (int c = colStart; c < colEnd && c < GlyphWidth; c++) mask |= (byte)(0x80 >> c);
            for (int r = 0; r < GlyphHeight; r++) g[r] = mask;
            return g;
        }

        // Shade pattern: density 1=light(25%), 2=medium(50%), 3=dark(75%).
        private static byte[] ShadeGlyph(int density)
        {
            byte[] g = new byte[GlyphHeight];
            for (int r = 0; r < GlyphHeight; r++)
            {
                byte b = 0;
                for (int c = 0; c < GlyphWidth; c++)
                {
                    bool on;
                    switch (density)
                    {
                        case 1: on = ((r & 1) == 0) && ((c & 1) == 0); break;            // ░ ~25%
                        case 2: on = ((r + c) & 1) == 0; break;                            // ▒ ~50%
                        case 3: on = !(((r & 1) == 1) && ((c & 1) == 1)); break;          // ▓ ~75%
                        default: on = false; break;
                    }
                    if (on) b |= (byte)(0x80 >> c);
                }
                g[r] = b;
            }
            return g;
        }

        // Build an 8x16 bitmap from a multi-line string (16 lines of 8 chars).
        // '.' or ' ' = off, anything else = on.
        private static byte[] B(string pattern)
        {
            var lines = pattern.Replace("\r", "").Split('\n');
            byte[] g = new byte[GlyphHeight];
            int row = 0;
            for (int i = 0; i < lines.Length && row < GlyphHeight; i++)
            {
                if (lines[i].Length == 0) continue;
                byte b = 0;
                for (int c = 0; c < GlyphWidth && c < lines[i].Length; c++)
                {
                    char ch = lines[i][c];
                    if (ch != '.' && ch != ' ') b |= (byte)(0x80 >> c);
                }
                g[row++] = b;
            }
            return g;
        }

