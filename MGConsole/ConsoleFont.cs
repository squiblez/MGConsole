        public const int FirstChar = 32; // Space
        public const int LastChar = 126; // '~'
        public const int CharCount = LastChar - FirstChar + 1;

        // Scaled glyph dimensions used for layout and rendering.
        public int ScaledWidth { get; }
        public int ScaledHeight { get; }

        private Texture2D _fontTexture;
        private Texture2D _pixel;
        private Rectangle[] _glyphRects;
        private readonly Dictionary<int, Rectangle> _extendedGlyphs = new();

        public Texture2D FontTexture => _fontTexture;
        public Texture2D Pixel => _pixel;
        public Rectangle[] GlyphRects => _glyphRects;

        // Look up the glyph rectangle for any supported codepoint
        // (ASCII 32-126 plus the extended set built in BuildExtraGlyphs()).
        public bool TryGetGlyph(int codepoint, out Rectangle rect)
        {
            if (codepoint >= FirstChar && codepoint <= LastChar)
            {
                rect = _glyphRects[codepoint - FirstChar];
                return true;
            }
            return _extendedGlyphs.TryGetValue(codepoint, out rect);
        }

        public ConsoleFont(GraphicsDevice graphicsDevice, float fontScale = 1.0f)
        {
            // Round to the nearest pixel so the grid stays on whole-pixel boundaries.
            ScaledWidth  = Math.Max(1, (int)Math.Round(GlyphWidth  * fontScale));
            ScaledHeight = Math.Max(1, (int)Math.Round(GlyphHeight * fontScale));

            _pixel = new Texture2D(graphicsDevice, 1, 1);
            _pixel.SetData(new[] { Color.White });

            // Build the list of extended (non-ASCII) glyphs to bake into the atlas.
            var extras = BuildExtraGlyphs();

            // Generate the font texture: ASCII 32-126 followed by extended glyphs.
            _glyphRects = new Rectangle[CharCount];
            int cols = 16;
            int totalCount = CharCount + extras.Count;
            int rows = (totalCount + cols - 1) / cols;
            int texWidth = cols * GlyphWidth;
            int texHeight = rows * GlyphHeight;
            Color[] data = new Color[texWidth * texHeight];

            for (int i = 0; i < CharCount; i++)
            {
                int ch = FirstChar + i;
                int col = i % cols;
                int row = i / cols;
                int x = col * GlyphWidth;
                int y = row * GlyphHeight;
                _glyphRects[i] = new Rectangle(x, y, GlyphWidth, GlyphHeight);
                DrawGlyph(data, texWidth, texHeight, x, y, ch);
            }

            for (int i = 0; i < extras.Count; i++)
            {
                int slot = CharCount + i;
                int col = slot % cols;
                int row = slot / cols;
                int x = col * GlyphWidth;
                int y = row * GlyphHeight;
                var (codepoint, bits) = extras[i];
                _extendedGlyphs[codepoint] = new Rectangle(x, y, GlyphWidth, GlyphHeight);
                DrawBits(data, texWidth, texHeight, x, y, bits);
            }

            _fontTexture = new Texture2D(graphicsDevice, texWidth, texHeight);
            _fontTexture.SetData(data);
        }

